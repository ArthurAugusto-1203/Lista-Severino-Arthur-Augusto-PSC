package lista4exercicio02;

public class Lista4exercicio02 {

    public static void main(String[] args) {

        int numero = 0;
        System.out.println("Aumentando o numero de 1000 em 1000 apartir desta mensagem: ");
        while (numero <= 100000) {
            System.out.println(numero);
            numero = numero + 1000;
        }
    }

}
